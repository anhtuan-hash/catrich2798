create or replace function public.professional_hub_validate_member_links()
returns trigger
language plpgsql
security invoker
set search_path = public, pg_temp
as $$
begin
  if tg_table_name = 'professional_hub_task_assignees' then
    perform public.assert_professional_hub_active_member(
      new.hub_id,
      new.user_id,
      'assignee'
    );
  elsif tg_table_name = 'professional_hub_plans' then
    perform public.assert_professional_hub_active_member(
      new.hub_id,
      new.owner_id,
      'plan owner'
    );
  elsif tg_table_name = 'professional_hub_records' then
    perform public.assert_professional_hub_active_member(
      new.hub_id,
      new.owner_id,
      'record owner'
    );
  elsif tg_table_name = 'professional_hub_evidence' then
    perform public.assert_professional_hub_active_member(
      new.hub_id,
      new.owner_id,
      'evidence owner'
    );
  elsif tg_table_name = 'professional_hub_notifications' then
    perform public.assert_professional_hub_active_member(
      new.hub_id,
      new.recipient_id,
      'notification recipient'
    );
  elsif tg_table_name = 'professional_hub_meetings' then
    perform public.assert_professional_hub_active_member(
      new.hub_id,
      new.chair_id,
      'meeting chair'
    );
    perform public.assert_professional_hub_active_member(
      new.hub_id,
      new.secretary_id,
      'meeting secretary'
    );
  end if;

  return new;
end;
$$;


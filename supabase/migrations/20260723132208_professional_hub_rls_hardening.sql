-- ---------------------------------------------------------------------------
-- 6. Assignment workflow guard
-- ---------------------------------------------------------------------------

create or replace function public.professional_hub_guard_task_assignee_update()
returns trigger
language plpgsql
security invoker
set search_path = public, pg_temp
as $$
declare
  caller_id uuid := auth.uid();
  caller_is_leader boolean;
begin
  if new.task_id is distinct from old.task_id
    or new.hub_id is distinct from old.hub_id
    or new.user_id is distinct from old.user_id
  then
    raise exception 'Task assignment identity cannot be changed'
      using errcode = '23514';
  end if;

  if caller_id is null then
    return new;
  end if;

  caller_is_leader := public.has_professional_hub_role(
    old.hub_id,
    array['leader']
  );

  if caller_is_leader then
    if new.status = 'completed' then
      new.completed_at := coalesce(new.completed_at, now());
      new.progress := 100;
    elsif new.status <> 'completed' then
      new.completed_at := null;
    end if;

    return new;
  end if;

  if caller_id <> old.user_id then
    raise exception 'Teachers may update only their own assignments'
      using errcode = '42501';
  end if;

  if (
    to_jsonb(new)
      - array[
          'status',
          'progress',
          'note',
          'submitted_at',
          'updated_at'
        ]
  ) is distinct from (
    to_jsonb(old)
      - array[
          'status',
          'progress',
          'note',
          'submitted_at',
          'updated_at'
        ]
  ) then
    raise exception 'Teachers may update only progress, note and submission state'
      using errcode = '42501';
  end if;

  if not (
    (old.status = 'assigned'
      and new.status in ('assigned', 'in_progress', 'submitted'))
    or
    (old.status = 'in_progress'
      and new.status in ('in_progress', 'submitted'))
    or
    (old.status = 'returned'
      and new.status in ('returned', 'in_progress', 'submitted'))
    or
    (old.status = 'submitted'
      and new.status = 'submitted')
    or
    (old.status = 'completed'
      and new.status = 'completed')
  ) then
    raise exception 'Invalid teacher task-status transition: % -> %',
      old.status,
      new.status
      using errcode = '23514';
  end if;

  if new.status = 'submitted' then
    new.submitted_at := coalesce(new.submitted_at, now());
  end if;

  new.completed_at := old.completed_at;

  return new;
end;
$$;

drop trigger if exists professional_hub_task_assignees_update_guard
  on public.professional_hub_task_assignees;

create trigger professional_hub_task_assignees_update_guard
before update
on public.professional_hub_task_assignees
for each row
execute function public.professional_hub_guard_task_assignee_update();


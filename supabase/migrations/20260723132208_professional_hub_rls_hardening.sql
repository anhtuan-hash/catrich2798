create or replace function public.professional_hub_guard_reviewable_update()
returns trigger
language plpgsql
security invoker
set search_path = public, pg_temp
as $$
declare
  caller_id uuid := auth.uid();
  caller_is_owner boolean;
  caller_is_leader boolean;
  allowed boolean := false;
begin
  if new.hub_id is distinct from old.hub_id
    or new.owner_id is distinct from old.owner_id
  then
    raise exception 'Reviewable item identity cannot be changed'
      using errcode = '23514';
  end if;

  if caller_id is null then
    return new;
  end if;

  caller_is_owner := caller_id = old.owner_id;
  caller_is_leader := public.has_professional_hub_role(
    old.hub_id,
    array['leader']
  );

  -- The owner path is checked first to prevent a leader approving
  -- an item that they personally own.
  if caller_is_owner then
    if old.status not in ('draft', 'needs_revision') then
      raise exception 'Owner cannot modify an item after submission'
        using errcode = '42501';
    end if;

    allowed := (
      new.status = old.status
      or new.status = 'submitted'
    );

    if not allowed then
      raise exception 'Owner may only save or submit the item'
        using errcode = '23514';
    end if;

    if tg_table_name = 'professional_hub_plans' then
      new.reviewer_id := old.reviewer_id;
      new.approved_at := old.approved_at;
      new.review_note := old.review_note;
    elsif tg_table_name = 'professional_hub_records' then
      new.reviewer_id := old.reviewer_id;
      new.reviewed_at := old.reviewed_at;
      new.feedback := old.feedback;
    elsif tg_table_name = 'professional_hub_evidence' then
      new.verified_by := old.verified_by;
      new.verified_at := old.verified_at;
    end if;

    if new.status = 'submitted'
      and tg_table_name = 'professional_hub_records'
    then
      new.submitted_at := coalesce(new.submitted_at, now());
    end if;

    return new;
  end if;

  if not caller_is_leader then
    raise exception 'Only the owner or Hub leader may update this item'
      using errcode = '42501';
  end if;

  if old.status <> 'submitted' then
    raise exception 'Leader may review only submitted items'
      using errcode = '23514';
  end if;

  if tg_table_name = 'professional_hub_evidence' then
    if new.status not in ('verified', 'rejected') then
      raise exception 'Evidence review must end as verified or rejected'
        using errcode = '23514';
    end if;

    new.verified_by := caller_id;
    new.verified_at := now();
  else
    if new.status not in ('approved', 'needs_revision') then
      raise exception 'Review must end as approved or needs_revision'
        using errcode = '23514';
    end if;

    new.reviewer_id := caller_id;

    if tg_table_name = 'professional_hub_plans' then
      new.approved_at := case
        when new.status = 'approved' then now()
        else null
      end;
    elsif tg_table_name = 'professional_hub_records' then
      new.reviewed_at := now();
    end if;
  end if;

  return new;
end;
$$;


-- ---------------------------------------------------------------------------
-- 8. Notification update guard: recipient may change read_at only
-- ---------------------------------------------------------------------------

create or replace function public.professional_hub_guard_notification_update()
returns trigger
language plpgsql
security invoker
set search_path = public, pg_temp
as $$
begin
  if auth.uid() is null then
    return new;
  end if;

  if auth.uid() <> old.recipient_id then
    raise exception 'Only the recipient may update a notification'
      using errcode = '42501';
  end if;

  if (
    to_jsonb(new) - array['read_at']
  ) is distinct from (
    to_jsonb(old) - array['read_at']
  ) then
    raise exception 'Notification recipients may update read_at only'
      using errcode = '42501';
  end if;

  return new;
end;
$$;

drop trigger if exists professional_hub_notifications_update_guard
  on public.professional_hub_notifications;

create trigger professional_hub_notifications_update_guard
before update
on public.professional_hub_notifications
for each row
execute function public.professional_hub_guard_notification_update();


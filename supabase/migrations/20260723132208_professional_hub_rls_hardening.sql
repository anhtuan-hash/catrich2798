  public.has_professional_hub_role(hub_id, array['leader'])
);

revoke insert, update, delete
  on public.professional_hub_audit_log
  from authenticated;

create or replace function public.professional_hub_write_audit_log()
returns trigger
language plpgsql
security definer
set search_path = public, pg_temp
as $$
declare
  source_row jsonb;
  target_row jsonb;
  target_hub_id uuid;
  target_entity_id uuid;
begin
  source_row := case
    when tg_op in ('UPDATE', 'DELETE') then to_jsonb(old)
    else null
  end;

  target_row := case
    when tg_op in ('INSERT', 'UPDATE') then to_jsonb(new)
    else null
  end;

  target_hub_id := coalesce(
    (target_row ->> 'hub_id')::uuid,
    (source_row ->> 'hub_id')::uuid
  );

  target_entity_id := coalesce(
    nullif(target_row ->> 'id', '')::uuid,
    nullif(source_row ->> 'id', '')::uuid
  );

  insert into public.professional_hub_audit_log (
    hub_id,
    actor_id,
    action,
    entity_type,
    entity_id,
    old_state,
    new_state
  )
  values (
    target_hub_id,
    auth.uid(),
    lower(tg_op),
    tg_table_name,
    target_entity_id,
    source_row,
    target_row
  );

  return coalesce(new, old);
end;
$$;

revoke all
  on function public.professional_hub_write_audit_log()
  from public;

-- ---------------------------------------------------------------------------
-- 2. Reusable active-membership assertion
-- ---------------------------------------------------------------------------

create or replace function public.assert_professional_hub_active_member(
  target_hub_id uuid,
  target_user_id uuid,
  field_name text default 'user'
)
returns void
language plpgsql
stable
security definer
set search_path = public, pg_temp
as $$
begin
  if target_user_id is null then
    return;
  end if;

  if not exists (
    select 1
    from public.professional_hub_members membership
    where membership.hub_id = target_hub_id
      and membership.user_id = target_user_id
      and membership.membership_status = 'active'
  ) then
    raise exception '% must be an active member of the Professional Hub',
      field_name
      using errcode = '23514';
  end if;
end;
$$;

revoke all
  on function public.assert_professional_hub_active_member(uuid, uuid, text)
  from public;

grant execute
  on function public.assert_professional_hub_active_member(uuid, uuid, text)
  to authenticated;

-- ---------------------------------------------------------------------------
-- 3. Membership protection
-- ---------------------------------------------------------------------------

create or replace function public.professional_hub_guard_membership()
returns trigger
language plpgsql
security invoker
set search_path = public, pg_temp
as $$
declare
  remaining_leaders integer;
begin
  if tg_op = 'UPDATE' then
    if new.hub_id is distinct from old.hub_id
      or new.user_id is distinct from old.user_id
    then
      raise exception 'Membership identity cannot be changed'
        using errcode = '23514';
    end if;
  end if;

  if old.role = 'leader'
    and old.membership_status = 'active'
    and (
      tg_op = 'DELETE'
      or new.role is distinct from 'leader'
      or new.membership_status is distinct from 'active'
    )
  then
    select count(*)
    into remaining_leaders
    from public.professional_hub_members membership
    where membership.hub_id = old.hub_id
      and membership.user_id <> old.user_id
      and membership.role = 'leader'
      and membership.membership_status = 'active';

    if remaining_leaders = 0 then
      raise exception 'A Professional Hub must retain at least one active leader'
        using errcode = '23514';
    end if;
  end if;

  return coalesce(new, old);
end;
$$;

drop trigger if exists professional_hub_members_guard
  on public.professional_hub_members;

create trigger professional_hub_members_guard
before update or delete
on public.professional_hub_members
for each row
execute function public.professional_hub_guard_membership();


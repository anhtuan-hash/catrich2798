create table if not exists public.professional_hub_audit_log (
  id bigint generated by default as identity primary key,
  hub_id uuid not null
    references public.professional_hubs(id) on delete cascade,
  actor_id uuid references auth.users(id) on delete set null,
  action text not null,
  entity_type text not null,
  entity_id uuid,
  old_state jsonb,
  new_state jsonb,
  created_at timestamptz not null default now()
);

create index if not exists professional_hub_audit_log_hub_created_idx
  on public.professional_hub_audit_log(hub_id, created_at desc);

alter table public.professional_hub_audit_log
  enable row level security;


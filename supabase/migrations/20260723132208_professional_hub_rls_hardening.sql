-- 9. Immutable task identity
-- ---------------------------------------------------------------------------

create or replace function public.professional_hub_guard_task_identity()
returns trigger
language plpgsql
security invoker
set search_path = public, pg_temp
as $$
begin
  if new.hub_id is distinct from old.hub_id
    or new.created_by is distinct from old.created_by
  then
    raise exception 'Task hub and creator cannot be changed'
      using errcode = '23514';
  end if;

  return new;
end;
$$;


create or replace function public.bes_v1133_is_leader(target_user uuid default auth.uid())
returns boolean
language plpgsql
stable
security definer
set search_path=public,auth,pg_temp
as $$
declare role_value text; jwt_role text;
begin
  if target_user is null then return false; end if;
  if to_regprocedure('public.bes_v1099_current_role(uuid)') is not null then
    execute 'select public.bes_v1099_current_role($1)' into role_value using target_user;
    if lower(coalesce(role_value,'')) in ('admin','department_head','head','ttcm','leader') then return true; end if;
  end if;
  if to_regclass('public.system_roles') is not null then
    select role into role_value from public.system_roles
    where user_id=target_user and active=true order by assigned_at desc nulls last limit 1;
    if lower(coalesce(role_value,'')) in ('admin','department_head','head','ttcm','leader') then return true; end if;
  end if;
  if to_regclass('public.profiles') is not null then
    begin
      execute 'select role from public.profiles where coalesce(id,user_id,profile_id)=$1 limit 1'
      into role_value using target_user;
      if lower(coalesce(role_value,'')) in ('admin','department_head','head','ttcm','leader') then return true; end if;
    exception when others then null;
    end;
  end if;
  jwt_role := lower(coalesce(
    auth.jwt()->'app_metadata'->>'role',
    auth.jwt()->'user_metadata'->>'role',
    auth.jwt()->>'role',''
  ));
  return jwt_role in ('admin','administrator','department_head','head','ttcm','leader');
exception when others then return false;
end $$;
grant execute on function public.bes_v1133_is_leader(uuid) to authenticated;


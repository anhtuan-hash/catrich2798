-- Atomic teacher response: comment + attachments + submitted status in one transaction.
create or replace function public.bes_v1133_submit_work_response(
  target_item uuid,
  response_body text,
  response_attachments jsonb default '[]'::jsonb
) returns jsonb
language plpgsql
security definer
set search_path=public,auth,pg_temp
as $$
declare uid uuid:=auth.uid(); item public.work_hub_items%rowtype; response public.work_hub_comments%rowtype;
begin
  if uid is null then raise exception 'Authentication required'; end if;
  select * into item from public.work_hub_items where id=target_item for update;
  if not found then raise exception 'Work item not found'; end if;
  if not(uid=any(coalesce(item.assignee_ids,'{}'::uuid[])) or public.bes_v1133_is_leader(uid)) then
    raise exception 'You are not allowed to submit this work item';
  end if;
  if jsonb_typeof(coalesce(response_attachments,'[]'::jsonb))<>'array' then
    raise exception 'Attachments must be a JSON array';
  end if;
  insert into public.work_hub_comments(item_id,author_id,body,comment_type,attachments)
  values(target_item,uid,coalesce(nullif(btrim(response_body),''),'Đã nộp phản hồi công việc.'),'submission',coalesce(response_attachments,'[]'::jsonb))
  returning * into response;
  update public.work_hub_items
  set status='submitted',submitted_at=now(),updated_at=now()
  where id=target_item returning * into item;
  return jsonb_build_object('comment',to_jsonb(response),'item',to_jsonb(item));
end $$;
grant execute on function public.bes_v1133_submit_work_response(uuid,text,jsonb) to authenticated;


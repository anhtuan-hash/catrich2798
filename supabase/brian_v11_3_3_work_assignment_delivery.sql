  for p in select policyname from pg_policies where schemaname='storage' and tablename='objects' and policyname like 'work_hub_files_%_v1133'
  loop execute format('drop policy if exists %I on storage.objects',p.policyname); end loop;
end $do$;

create policy work_hub_files_select_v1133 on storage.objects
for select to authenticated using(
  bucket_id='work-hub-submissions'
  and exists(
    select 1 from public.work_hub_items i
    where i.id::text=(storage.foldername(name))[1]
      and public.bes_v1133_can_view_work_item(i,auth.uid())
  )
);
create policy work_hub_files_insert_v1133 on storage.objects
for insert to authenticated with check(
  bucket_id='work-hub-submissions'
  and (storage.foldername(name))[2]=auth.uid()::text
  and exists(
    select 1 from public.work_hub_items i
    where i.id::text=(storage.foldername(name))[1]
      and public.bes_v1133_can_view_work_item(i,auth.uid())
  )
);
create policy work_hub_files_delete_v1133 on storage.objects
for delete to authenticated using(
  bucket_id='work-hub-submissions'
  and (
    (storage.foldername(name))[2]=auth.uid()::text
    or public.bes_v1133_is_leader(auth.uid())
  )
);


for insert to authenticated with check(
  created_by=auth.uid() and owner_id=auth.uid()
  and (public.bes_v1133_is_leader(auth.uid()) or assignee_ids<@array[auth.uid()]::uuid[])
);
create policy work_hub_items_update_v1133 on public.work_hub_items
for update to authenticated using(public.bes_v1133_can_view_work_item(work_hub_items,auth.uid()))
with check(public.bes_v1133_can_view_work_item(work_hub_items,auth.uid()));
create policy work_hub_comments_select_v1133 on public.work_hub_comments
for select to authenticated using(exists(select 1 from public.work_hub_items i where i.id=item_id and public.bes_v1133_can_view_work_item(i,auth.uid())));
create policy work_hub_comments_insert_v1133 on public.work_hub_comments
for insert to authenticated with check(author_id=auth.uid() and exists(select 1 from public.work_hub_items i where i.id=item_id and public.bes_v1133_can_view_work_item(i,auth.uid())));
create policy work_hub_activity_select_v1133 on public.work_hub_activity

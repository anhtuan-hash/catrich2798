create or replace function public.bes_v1133_can_view_work_item(target_item public.work_hub_items,target_user uuid default auth.uid())
returns boolean language sql stable security definer set search_path=public,auth,pg_temp as $$
  select target_user is not null and (
    public.bes_v1133_is_leader(target_user)
    or target_item.created_by=target_user
    or target_item.owner_id=target_user
    or target_user=any(coalesce(target_item.assignee_ids,'{}'::uuid[]))
    or target_user=any(coalesce(target_item.watcher_ids,'{}'::uuid[]))
    or target_item.visibility='department'
  );
$$;
grant execute on function public.bes_v1133_can_view_work_item(public.work_hub_items,uuid) to authenticated;

create or replace function public.bes_v1133_guard_work_item_update()
returns trigger language plpgsql security definer set search_path=public,auth,pg_temp as $$
declare uid uuid:=auth.uid(); old_core jsonb; new_core jsonb;
begin
  if uid is null then raise exception 'Authentication required'; end if;
  new.updated_at:=now();
  if public.bes_v1133_is_leader(uid) or old.created_by=uid or old.owner_id=uid then return new; end if;
  if uid=any(coalesce(old.assignee_ids,'{}'::uuid[])) then
    old_core:=to_jsonb(old)-array['status','submitted_at','completed_at','updated_at'];
    new_core:=to_jsonb(new)-array['status','submitted_at','completed_at','updated_at'];
    if old_core<>new_core then raise exception 'Assignees can only update workflow status'; end if;
    if new.status=old.status then return new; end if;
    if not(
      (old.status='assigned' and new.status in ('accepted','in_progress','submitted'))
      or (old.status='accepted' and new.status in ('in_progress','submitted'))
      or (old.status='in_progress' and new.status='submitted')
      or (old.status='changes_requested' and new.status in ('in_progress','submitted'))
    ) then raise exception 'Invalid assignee workflow transition: % -> %',old.status,new.status; end if;
    if new.status='submitted' then new.submitted_at:=coalesce(new.submitted_at,now()); end if;
    return new;
  end if;
  raise exception 'You cannot update this work item';
end $$;

drop trigger if exists bes_v1133_work_item_guard on public.work_hub_items;
create trigger bes_v1133_work_item_guard before update on public.work_hub_items
for each row execute function public.bes_v1133_guard_work_item_update();

create or replace function public.bes_v1133_notify_work_users(
  target_item uuid,target_users uuid[],target_type text,target_title text,target_body text,actor uuid default auth.uid()
) returns void language plpgsql security definer set search_path=public,auth,pg_temp as $$
declare recipient uuid;
begin
  foreach recipient in array coalesce(target_users,'{}'::uuid[]) loop
    if recipient is not null and recipient is distinct from actor then
      insert into public.work_hub_notifications(user_id,item_id,notification_type,title,body)
      values(recipient,target_item,target_type,left(target_title,240),left(coalesce(target_body,''),1000));
    end if;
  end loop;
end $$;

create or replace function public.bes_v1133_after_work_item_insert()
returns trigger language plpgsql security definer set search_path=public,auth,pg_temp as $$
declare recipients uuid[];
begin
  insert into public.work_hub_activity(item_id,actor_id,event_type,to_status,message,payload)
  values(new.id,new.created_by,'created',new.status,'Đã tạo và giao công việc',jsonb_build_object('title',new.title));
  select array_agg(distinct u) into recipients
  from unnest(coalesce(new.assignee_ids,'{}'::uuid[])||coalesce(new.watcher_ids,'{}'::uuid[])) u
  where u is not null;
  perform public.bes_v1133_notify_work_users(
    new.id,recipients,'work_item_assigned','Công việc mới: '||new.title,
    case when new.due_at is null then 'Admin/TTCM vừa giao một công việc mới.'
         else 'Hạn hoàn thành: '||to_char(new.due_at at time zone 'Asia/Ho_Chi_Minh','DD/MM/YYYY HH24:MI') end,
    new.created_by
  );
  return new;
end $$;

create or replace function public.bes_v1133_after_work_item_update()
returns trigger language plpgsql security definer set search_path=public,auth,pg_temp as $$
declare actor uuid:=auth.uid(); recipients uuid[]; notice_type text; notice_title text; notice_body text;
begin
  if new.status is distinct from old.status then
    insert into public.work_hub_activity(item_id,actor_id,event_type,from_status,to_status,message,payload)
    values(new.id,actor,'status_changed',old.status,new.status,'Đã cập nhật trạng thái công việc',jsonb_build_object('title',new.title));
    select array_agg(distinct u) into recipients
    from unnest(
      coalesce(new.assignee_ids,'{}'::uuid[])
      ||coalesce(new.watcher_ids,'{}'::uuid[])
      ||array[new.owner_id,new.created_by]
    ) u where u is not null;
    if new.status='submitted' then
      notice_type:='work_item_submission';
      notice_title:='Đã nộp công việc: '||new.title;
      notice_body:='Giáo viên đã gửi phản hồi hoặc tệp đính kèm. Mở Trung tâm công việc để xem.';
    else
      notice_type:='work_item_status';
      notice_title:='Cập nhật công việc: '||new.title;
      notice_body:='Trạng thái mới: '||new.status;
    end if;
    perform public.bes_v1133_notify_work_users(new.id,recipients,notice_type,notice_title,notice_body,actor);
  end if;
  return new;
end $$;

create or replace function public.bes_v1133_after_work_comment_insert()
returns trigger language plpgsql security definer set search_path=public,auth,pg_temp as $$
declare item public.work_hub_items%rowtype; recipients uuid[];
begin
  insert into public.work_hub_activity(item_id,actor_id,event_type,message,payload)
  values(new.item_id,new.author_id,case when new.comment_type='submission' then 'response_submitted' else 'comment_added' end,
    case when new.comment_type='submission' then 'Giáo viên đã nộp phản hồi' else 'Đã thêm phản hồi' end,
    jsonb_build_object('comment_id',new.id,'comment_type',new.comment_type));
  if new.comment_type='submission' then return new; end if;
  select * into item from public.work_hub_items where id=new.item_id;
  select array_agg(distinct u) into recipients
  from unnest(
    coalesce(item.assignee_ids,'{}'::uuid[])
    ||coalesce(item.watcher_ids,'{}'::uuid[])
    ||array[item.owner_id,item.created_by]
  ) u where u is not null;
  perform public.bes_v1133_notify_work_users(item.id,recipients,'work_item_comment','Phản hồi mới: '||item.title,left(new.body,300),new.author_id);
  return new;
end $$;


pass('structured credit-limit error is preserved',gemini.includes("error.code = 'AI_PROVIDER_CREDIT_LIMIT'")&&gemini.includes('capacityError'));
pass('AI Workspace imports local fallback engine',workspace.includes('buildWorkspaceLocalFallback')&&workspace.includes('friendlyAiWorkspaceError'));

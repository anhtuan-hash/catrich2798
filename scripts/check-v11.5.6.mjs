pass('package version 11.5.6',pkg.version==='11.5.6');
pass('version registry 11.5.6',version.version==='11.5.6'&&release.version==='11.5.6');
pass('runtime core 2.5.6',version.runtimeCore==='2.5.6'&&release.runtime==='2.5.6');

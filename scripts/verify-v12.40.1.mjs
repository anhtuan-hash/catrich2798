must(main.includes('aiGatewayReady || getProviderSummary().hasKey'), 'Global AI readiness still depends only on browser key state.');
must(textLab.includes('OPENROUTER_API_KEY trên Vercel'), 'TextLab gateway guidance was not updated.');
console.log('V12.40.1 verification passed: credit-aware free fallback, smaller TextLab budget, and global gateway readiness bridge are installed.');
